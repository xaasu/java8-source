    /**
     * addWorker的四种调用场景:
     * addWorker(command, true)：线程数<coreSize，将task放入，如果此时>coreSize，返回false
     * addWorker(command, false)：阻塞队列满，将新的task放入workers，如果此时>maxSize，返回false
     * addWorker(null, false)：放入一个空的task到Worker，此时线程限制数是maxSize，相当于创建一个新线程没分配任务
     * addWorker(null, true)：放入一个空的task到Worker，此时线程数 < coreSize
     */

    /**
     * 为线程分配worker执行
     *
     * @param firstTask：当前任务
     * @param core：是否加到核心线程
     * @return
     */
    private boolean addWorker(Runnable firstTask, boolean core) {
        //判断线程数量是否够，不够的话新增线程
        retry:
        for (; ; ) {
            int c = ctl.get();
            //当前线程池状态
            int rs = runStateOf(c);

            //看不懂
            // rs >= SHUTDOWN && (rs != SHUTDOWN || firstTask != null ||workQueue.isEmpty())
            //三种情况：
            // rs > SHUTDOWN：此时不再接受新的任务，且所有任务执行结束
            // rs = SHUTDOWN && firstTask != null：此时不再接受任务，但是仍然会执行队列中的任务
            // rs = SHUTDOWN && firstTask == null && workQueue.isEmpty()：任务为null，队列也为空，那么不需要执行，这也就排除了另一种情况：SHUTDOWN状态但是队列不为空
            if (rs >= SHUTDOWN &&
                    !(rs == SHUTDOWN &&
                            firstTask == null &&
                            !workQueue.isEmpty()))
                return false;

            // 走到这的情形：
            // 1.线程池状态为RUNNING
            // 2.SHUTDOWN状态，但队列中还有任务需要执行
            for (; ; ) {
                //当前线程数量
                int wc = workerCountOf(c);
                if (wc >= CAPACITY ||
                        wc >= (core ? corePoolSize : maximumPoolSize))
                    //当前活动线程数大于核心线程数/最大线程数，不能加入worker
                    return false;
                //当前线程数+1
                if (compareAndIncrementWorkerCount(c))
                    break retry;
                //代码走到这个说明线程数添加失败，重新读取ctl，下一轮循环
                c = ctl.get();  // Re-read ctl
                //判断线程池状态是否变化
                if (runStateOf(c) != rs)
                    //如果线程池状态发生变化（只有running状态才接受新任务），则跳到外层循环
                    continue retry;
                //如果状态没变化，继续尝试增加线程数量
            }
        }
        //代码执行到此处说明worker的数量成功+1，构造worker
        boolean workerStarted = false;
        boolean workerAdded = false;
        Worker w = null;
        try {
            //将本次的任务封装到worker
            w = new Worker(firstTask);
            //真正用来执行任务的线程
            final Thread t = w.thread;
            if (t != null) {
                final ReentrantLock mainLock = this.mainLock;
                mainLock.lock();
                try {
                    // Recheck while holding lock.
                    // Back out on ThreadFactory failure or if
                    // shut down before lock acquired.
                    int rs = runStateOf(ctl.get());
                    //当前状态为running或者(当前状态为shutdown并且任务为空)
                    if (rs < SHUTDOWN ||
                            (rs == SHUTDOWN && firstTask == null)) {
                        //线程状态检查
                        //Thread.isAlive()：当前线程是否可用，如果线程已经启动并且当前没有任何异常则是true，否则false
                        if (t.isAlive())
                            //线程状态异常，已经有任务占用了目标线程
                            throw new IllegalThreadStateException();
                        //worker加入workers中
                        workers.add(w);
                        int s = workers.size();
                        //更新运行最大线程数的统计
                        if (s > largestPoolSize)
                            largestPoolSize = s;
                        workerAdded = true;
                    }
                } finally {
                    mainLock.unlock();
                }
                //worker添加成功
                if (workerAdded) {
                    //启动worker内部的线程，其会调用worker内部的run方法
                    t.start();
                    workerStarted = true;
                }
            }
        } finally {
            if (!workerStarted)
                addWorkerFailed(w);
        }
        return workerStarted;
    }

    /**
     * 如果添加worker失败
     * - 将worker移出workers队列
     * - 当前工作线程数-1
     * - rechecks for termination, in case the existence of this
     * worker was holding up termination
     */
    private void addWorkerFailed(Worker w) {
        final ReentrantLock mainLock = this.mainLock;
        mainLock.lock();
        try {
            if (w != null)
                workers.remove(w);
            decrementWorkerCount();
            //
            tryTerminate();
        } finally {
            mainLock.unlock();
        }
    }

    /**
     * Performs cleanup and bookkeeping for a dying worker. Called
     * only from worker threads. Unless completedAbruptly is set,
     * assumes that workerCount has already been adjusted to account
     * for exit.  This method removes thread from worker set, and
     * possibly terminates the pool or replaces the worker if either
     * it exited due to user task exception or if fewer than
     * corePoolSize workers are running or queue is non-empty but
     * there are no workers.
     *
     * @param w                 the worker
     * @param completedAbruptly if the worker died due to user exception
     */
    private void processWorkerExit(Worker w, boolean completedAbruptly) {
        if (completedAbruptly) // If abrupt, then workerCount wasn't adjusted
            decrementWorkerCount();

        final ReentrantLock mainLock = this.mainLock;
        mainLock.lock();
        try {
            completedTaskCount += w.completedTasks;
            workers.remove(w);
        } finally {
            mainLock.unlock();
        }

        tryTerminate();

        int c = ctl.get();
        if (runStateLessThan(c, STOP)) {
            if (!completedAbruptly) {
                int min = allowCoreThreadTimeOut ? 0 : corePoolSize;
                if (min == 0 && !workQueue.isEmpty())
                    min = 1;
                if (workerCountOf(c) >= min)
                    return; // replacement not needed
            }
            addWorker(null, false);
        }
    }

    /**
     * 从阻塞队列中获取task
     *
     * @return
     */
    private Runnable getTask() {
        boolean timedOut = false; // Did the last poll() time out?

        for (; ; ) {
            int c = ctl.get();
            int rs = runStateOf(c);

            // Check if queue empty only if necessary.
            if (rs >= SHUTDOWN && (rs >= STOP || workQueue.isEmpty())) {
                decrementWorkerCount();
                return null;
            }

            int wc = workerCountOf(c);

            //如果线程池允许线程超时或者当前线程数大于核心线程数，则会进行超时处理
            boolean timed = allowCoreThreadTimeOut || wc > corePoolSize;

            if ((wc > maximumPoolSize || (timed && timedOut))
                    && (wc > 1 || workQueue.isEmpty())) {
                if (compareAndDecrementWorkerCount(c))
                    return null;
                continue;
            }

            try {
                Runnable r = timed ?
                        workQueue.poll(keepAliveTime, TimeUnit.NANOSECONDS) :
                        workQueue.take();
                if (r != null)
                    return r;
                //超时
                timedOut = true;
            } catch (InterruptedException retry) {
                timedOut = false;
            }
        }
    }

    /**
     * 执行任务
     */
    final void runWorker(Worker w) {
        //当前线程
        Thread wt = Thread.currentThread();
        //目标任务
        Runnable task = w.firstTask;
        w.firstTask = null;
        //Worker的构造函数中抑制了线程中断setState(-1)，所以这里需要unlock从而允许中断
        w.unlock(); // allow interrupts
        // 用于标识是否异常终止，finally中processWorkerExit的方法会有不同逻辑
        // 为true的情况：1.执行任务抛出异常；2.被中断。
        boolean completedAbruptly = true;
        try {
            //如果worker自己的task为null，调用getTask从阻塞队列获取等待任务执行，否则，阻塞该方法
            while (task != null || (task = getTask()) != null) {
                w.lock();
                //如果线程池已经停止，中止当前线程
                if ((runStateAtLeast(ctl.get(), STOP) ||
                        (Thread.interrupted() &&
                                runStateAtLeast(ctl.get(), STOP))) &&
                        !wt.isInterrupted())
                    wt.interrupt();
                try {
                    //beforeExecute定制方法，可以自己重写
                    beforeExecute(wt, task);
                    Throwable thrown = null;
                    try {
                        //执行目标方法
                        task.run();
                    } catch (RuntimeException x) {
                        thrown = x;
                        throw x;
                    } catch (Error x) {
                        thrown = x;
                        throw x;
                    } catch (Throwable x) {
                        thrown = x;
                        throw new Error(x);
                    } finally {
                        //afterExecute和beforeExecute一样，定制方法
                        afterExecute(task, thrown);
                    }
                } finally {
                    task = null;
                    w.completedTasks++;
                    w.unlock();
                }
            }
            completedAbruptly = false;
        } finally {
            processWorkerExit(w, completedAbruptly);
        }
    }

